        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include="Id,Email,Password,Name,Surname,Birthday")] User user)
        {
            if (ModelState.IsValid)
            {
                repo.CreateUser(user);
                return RedirectToAction("Index");
            }

            return View(user);
        }

